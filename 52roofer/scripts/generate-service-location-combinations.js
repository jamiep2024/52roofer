const fs = require('fs');
const path = require('path');
const glob = require('glob');

// Get all locations
const getLocations = () => {
  const locationFiles = glob.sync('pages/roofers-in-*.tsx');
  return locationFiles.map(file => {
    const match = file.match(/roofers-in-(.+)\.tsx$/);
    return match ? match[1] : null;
  }).filter(Boolean);
};

// Get core services
const getCoreServices = () => [
  'roof-repair',
  'roof-installation',
  'roof-maintenance',
  'emergency-roofing',
  'gutter-services',
  'roof-inspection'
];

// Template for combination pages
const generatePageContent = (service, location) => {
  const serviceTitle = service.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  const locationTitle = location.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  
  return `import React from 'react';
import { NextPage } from 'next';
import ServiceLayout from '@/components/ServiceLayout';

const ${serviceTitle.replace(/\s+/g, '')}In${locationTitle.replace(/\s+/g, '')}: NextPage = () => {
  return (
    <ServiceLayout
      title="${serviceTitle} in ${locationTitle}"
      description="Professional ${serviceTitle.toLowerCase()} services in ${locationTitle}. Expert local roofers, competitive pricing, and reliable service."
      location="${locationTitle}"
      service="${serviceTitle}"
    >
      {/* Content will be dynamically generated by ServiceLayout */}
    </ServiceLayout>
  );
};

export default ${serviceTitle.replace(/\s+/g, '')}In${locationTitle.replace(/\s+/g, '')};
`;
};

// Generate combination pages
const generateCombinations = () => {
  const locations = getLocations();
  const services = getCoreServices();
  const combinations = [];

  locations.forEach(location => {
    services.forEach(service => {
      const pagePath = `pages/services/${service}-in-${location}.tsx`;
      
      if (!fs.existsSync(pagePath)) {
        const content = generatePageContent(service, location);
        fs.writeFileSync(pagePath, content);
        combinations.push(`${service}-in-${location}`);
        console.log(`Generated: ${pagePath}`);
      }
    });
  });

  return combinations;
};

// Generate report
const generateReport = (combinations) => {
  console.log('\nService-Location Combination Pages Generated:');
  console.log('----------------------------------------');
  console.log(`Total new pages created: ${combinations.length}`);
  
  // Calculate total pages
  const existingPages = glob.sync('pages/**/*.tsx', {
    ignore: ['pages/_*.tsx', 'pages/api/**']
  }).length;
  
  console.log(`\nTotal Pages:`);
  console.log(`- Existing pages: ${existingPages - combinations.length}`);
  console.log(`- New combinations: ${combinations.length}`);
  console.log(`- Total: ${existingPages}`);
};

// Run generation
console.log('Generating service+location combination pages...\n');
const combinations = generateCombinations();
generateReport(combinations);
