const fs = require('fs');
const path = require('path');
const glob = require('glob');

// Get all locations
const getLocations = () => {
  const locationFiles = glob.sync('pages/roofers-in-*.tsx');
  return locationFiles.map(file => {
    const match = file.match(/roofers-in-(.+)\.tsx$/);
    return match ? match[1] : null;
  }).filter(Boolean);
};

// Get core services
const getCoreServices = () => [
  'roof-repair',
  'roof-installation',
  'roof-maintenance',
  'emergency-roofing',
  'gutter-services',
  'roof-inspection'
];

// Template for voice-enabled service pages
const generateVoiceServicePage = (service) => {
  const serviceTitle = service.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  
  return `import React from 'react';
import { NextPage } from 'next';
import ServiceLayout from '@/components/ServiceLayout';

const Voice${serviceTitle.replace(/\s+/g, '')}: NextPage = () => {
  return (
    <ServiceLayout
      title="Voice-Enabled ${serviceTitle}"
      description="Voice-optimized ${serviceTitle.toLowerCase()} services. Use voice commands to get information, request quotes, and schedule appointments."
      service="${serviceTitle}"
      voiceEnabled={true}
    >
      {/* Content will be dynamically generated by ServiceLayout */}
    </ServiceLayout>
  );
};

export default Voice${serviceTitle.replace(/\s+/g, '')};
`;
};

// Template for voice-enabled location pages
const generateVoiceLocationPage = (location) => {
  const locationTitle = location.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  
  return `import React from 'react';
import { NextPage } from 'next';
import LocationLayout from '@/components/LocationLayout';

const Voice${locationTitle.replace(/\s+/g, '')}: NextPage = () => {
  return (
    <LocationLayout
      title="Voice-Enabled Roofing Services in ${locationTitle}"
      description="Voice-optimized roofing services in ${locationTitle}. Use voice commands to find local roofers, get quotes, and schedule appointments."
      location="${locationTitle}"
      voiceEnabled={true}
    >
      {/* Content will be dynamically generated by LocationLayout */}
    </LocationLayout>
  );
};

export default Voice${locationTitle.replace(/\s+/g, '')};
`;
};

// Template for voice-enabled combination pages
const generateVoiceCombinationPage = (service, location) => {
  const serviceTitle = service.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  const locationTitle = location.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  
  return `import React from 'react';
import { NextPage } from 'next';
import ServiceLayout from '@/components/ServiceLayout';

const Voice${serviceTitle.replace(/\s+/g, '')}In${locationTitle.replace(/\s+/g, '')}: NextPage = () => {
  return (
    <ServiceLayout
      title="Voice-Enabled ${serviceTitle} in ${locationTitle}"
      description="Voice-optimized ${serviceTitle.toLowerCase()} services in ${locationTitle}. Use voice commands to get information, request quotes, and schedule appointments."
      location="${locationTitle}"
      service="${serviceTitle}"
      voiceEnabled={true}
    >
      {/* Content will be dynamically generated by ServiceLayout */}
    </ServiceLayout>
  );
};

export default Voice${serviceTitle.replace(/\s+/g, '')}In${locationTitle.replace(/\s+/g, '')};
`;
};

// Generate voice pages
const generateVoicePages = () => {
  const locations = getLocations();
  const services = getCoreServices();
  const voicePages = [];

  // Create voice services directory if it doesn't exist
  const voiceDir = 'pages/voice';
  if (!fs.existsSync(voiceDir)) {
    fs.mkdirSync(voiceDir);
  }

  // Generate voice-enabled service pages
  services.forEach(service => {
    const pagePath = `${voiceDir}/${service}.tsx`;
    fs.writeFileSync(pagePath, generateVoiceServicePage(service));
    voicePages.push(`voice/${service}`);
    console.log(`Generated: ${pagePath}`);
  });

  // Generate voice-enabled location pages
  locations.forEach(location => {
    const pagePath = `${voiceDir}/roofers-in-${location}.tsx`;
    fs.writeFileSync(pagePath, generateVoiceLocationPage(location));
    voicePages.push(`voice/roofers-in-${location}`);
    console.log(`Generated: ${pagePath}`);
  });

  // Generate voice-enabled combination pages
  locations.forEach(location => {
    services.forEach(service => {
      const pagePath = `${voiceDir}/${service}-in-${location}.tsx`;
      fs.writeFileSync(pagePath, generateVoiceCombinationPage(service, location));
      voicePages.push(`voice/${service}-in-${location}`);
      console.log(`Generated: ${pagePath}`);
    });
  });

  return voicePages;
};

// Generate report
const generateReport = (voicePages) => {
  console.log('\nVoice-Enabled Pages Generated:');
  console.log('---------------------------');
  console.log(`Total new voice pages created: ${voicePages.length}`);
  
  // Calculate total pages
  const existingPages = glob.sync('pages/**/*.tsx', {
    ignore: ['pages/_*.tsx', 'pages/api/**']
  }).length;
  
  console.log(`\nTotal Pages:`);
  console.log(`- Regular pages: ${existingPages - voicePages.length}`);
  console.log(`- Voice pages: ${voicePages.length}`);
  console.log(`- Total: ${existingPages}`);
};

// Run generation
console.log('Generating voice-enabled pages...\n');
const voicePages = generateVoicePages();
generateReport(voicePages);
